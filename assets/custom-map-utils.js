  export function updateMapStyles(mapData) {
    const worldMap = document.getElementById("worldMap");
    if (!worldMap) return;
  
    const paths = worldMap.querySelectorAll("path");
  
    paths.forEach((path) => {
      const countryCode = path.getAttribute("data-country-code");
  
      // Vérifie si l'élément path a un attribut "data-selected-color"
      const pathSelectedColor = path.getAttribute("data-selected-color")
        ? path.getAttribute("data-selected-color")
        : document.getElementById("unfilledColorPicker").value;
  
      // Assigne la couleur au code du pays dans mapData
      mapData[countryCode] = pathSelectedColor;
    });
  
    mapData["oceanColor"] = document.getElementById("oceanColorPicker").value;
    mapData["selectedColor"] = document.getElementById(
      "selectedColorPicker"
    ).value;
    mapData["unfilledColor"] = document.getElementById(
      "unfilledColorPicker"
    ).value;
    mapData["borderColor"] = document.getElementById("borderColorPicker").value;
  
    // Crée un objet JSON avec les données de mapData
    return mapData;
  }
  